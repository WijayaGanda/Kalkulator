package com.Wijaya;

public class Kalkulator {

    public double getPenjumlahan (double penjumlahan1, double penjumlahan2){
        double hasil = penjumlahan1 + penjumlahan2;
        return hasil;
    }

    public double getPengurangan (double pengurangan1, double pengurangan2){
        double hasil = pengurangan1 - pengurangan2;
        return hasil;
    }

    public double getPerkalian (double perkalian1, double perkalian2){
        double hasil = perkalian1 * perkalian2;
        return hasil;
    }

    public double getPembagian (double pembagian1, double pembagian2){
        double hasil = pembagian1 / pembagian2;
        return hasil;
    }

}
